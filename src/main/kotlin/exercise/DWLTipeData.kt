fun main() {
    // Tipe Data Byte
    var minByte: Byte = Byte.MIN_VALUE
    var age: Byte = 24
    var maxByte: Byte = Byte.MAX_VALUE

    println("Min Value Tipe Data Byte: $minByte")
    println("Umur: $age")
    println("Max Value Tipe Data Byte: $maxByte")
    println()

    // Tipe Data Int
    var minInt = Int.MIN_VALUE
    var heightOne = 180
    var heightTwo: Int = 190
    var maxInt: Int = Int.MAX_VALUE

    println("Min Value Tipe Data Int: $minInt")
    println("Tinggi Dua: $heightOne")
    println("Tinggi Dua: $heightTwo")
    println("Max Value Tipe Data Int: $maxInt")
    println()

    // Tipe Data Short
    var minShort: Short = Short.MIN_VALUE
    var distanceOne: Short = 1600
    var distanceTwo: Short = 1800
    var maxShort: Short = Short.MAX_VALUE

    println("Min Value Tipe Data Short: $minShort")
    println("Jarak Satu: $distanceOne")
    println("Jarak Dua: $distanceTwo")
    println("Max Value Tipe Data Short: $maxShort")
    println()

    // Tipe Data Long
    var minLong: Long = Long.MIN_VALUE
    var balanceOne: Long = 1000000L
    var balanceTwo: Long = 1400000L
    var maxLong: Long = Long.MAX_VALUE

    println("Min Value Tipe Data Long: $minLong")
    println("Saldo Satu: $balanceOne")
    println("Saldo Two: $balanceTwo")
    println("Max Value Tipe Data Long: $maxLong")
    println()
}